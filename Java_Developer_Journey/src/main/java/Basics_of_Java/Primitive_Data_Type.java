package Java_Developer_Journey.src.main.java.Basics_of_Java;

public class Primitive_Data_Type {
    public static void main(String[] args){
        // boolean
        boolean bt = true;
        System.out.println(bt);

        // byte
        byte n;
        n = 127;
        System.out.println(n); // prints 127

        // char
        char a = 'A';
        char b = 66;
        System.out.println(a); // prints A 
        System.out.println(b); // Displays B 

        // short 
        short m = 1234;
        System.out.println(m);

        // int
        int num1= 56782;
        System.out.println(num1);

        // long 
        long num2 = 1500000000L;
        System.out.println(num2);

        // float
        float num3 = 3.14f;
        System.out.println(num3);

        // double
        double num4 = 69.78d;
        System.out.println(num4);


    }
    
}
